        /// <summary>
        /// this function set position of card, if it is dropped on slot
        /// </summary>
        public static void DropOnSlot<Type>(Card card, CardSlot<Type> cardSlot) where Type: Card
        {
            if (card!=null && card.GetType()==typeof(Type) && !Mouse.IsButtonPressed(Mouse.Button.Left)) {

                if (cardSlot.avavailable == true)
                {
                    Vector2f SlotCenter = cardSlot.shape.Position + new Vector2f(36, 36);
                    Vector2f CardCenter = card.shape.Position + new Vector2f(35, 35);


                    if (DistBetwPoints(SlotCenter, CardCenter) < ObjectsBank.MaxDistanceSlot_Card)
                    {


                        if (cardSlot.CardInSlot != null)// if there is some card in this slot- chanege places
                        {
                            Card tmpCard = cardSlot.CardInSlot;
                            Slot slot = card.Slot;

                            slot.SetCardIn(tmpCard);
                            tmpCard.SetCardSlot(slot);
                        }
                        else
                        {
                            if (card.Slot != null) // very unpossible situation, all cards will be in one slot
                                card.Slot.SetCardIn(null);
                        }

                        cardSlot.SetCardIn(card);
                        card.SetCardSlot(cardSlot);// ZDECYDOWANIE POTRZEBA takieej funkcji !!!! trzeba pomyśleć
                    }
                }
                else
                {
                    GameObj.ListOfPopingUpInfo.Add(new PopUpInfo("You can not do that", ObjectsBank.GameFrame));

                }
            }
        }


        public static float DistBetwPoints(Vector2f P1, Vector2f P2)
        {
            return (int)(Math.Sqrt(Math.Pow((P1.X - P2.X),2) + Math.Pow((P1.Y - P2.Y),2)));
        }


        /// <summary>
        /// function to check if mouse is hoveren on object
        /// </summary
        public static Boolean CheckIfMouseHover(Vector2f SizeOfElement,Vector2f PositionOfElement, MyWindow window)
        {
            
            int x = Mouse.GetPosition(window).X;
            int y = Mouse.GetPosition(window).Y;
            if ((x >= PositionOfElement.X && x <= (PositionOfElement.X + SizeOfElement.X)) && (y >= PositionOfElement.Y && y <= PositionOfElement.Y + SizeOfElement.Y))
            { return true; }
            else
            { return false; }
        }

    
        public static Vector2f RotateVector(Vector2f vector, float degrees)
        {
            Vector2f result = new Vector2f();
           
            double Ddegrees = Math.PI * ((double)degrees) / 180.0;
            result.X = vector.X * (float)Math.Cos(Ddegrees) - vector.Y * (float)Math.Sin(Ddegrees);
            result.Y = vector.X * (float)Math.Sin(Ddegrees) + vector.Y * (float)Math.Cos(Ddegrees);
            return result;
        }


                 //function for dividing long text sended to info window. It divide text into string rows ("\n")
        public static String Dividing(String text, uint sizeOfCharacter, Vector2f WindowSize, float Padding)
        {
            String[] List = text.Split(' ');
            List<String> Rows = new List<String>();
            Text tmp = new Text("", ObjectsBank.MyFont, sizeOfCharacter);
            float MaxWidth = WindowSize.X - 2 * Padding;
            float MaxHeight = WindowSize.Y - Padding * 2;// 40 pixels is reserved for button below

            String row;
            String moved;
            String[] realyTmp;

            float test;

            foreach (String word in List)
            {
                tmp.DisplayedString += " " + word;
                test = tmp.GetLocalBounds().Width;
                if (test > MaxWidth)// if string is too long for one row
                {
                    realyTmp = tmp.DisplayedString.Split(' ');
                    moved = realyTmp[realyTmp.Length - 1];// take last string 

                    row = tmp.DisplayedString.Replace(moved, "\n");
                    Rows.Add((string)row.Clone());
                    tmp.DisplayedString = moved;// string which can't go to upper row goes to lower row
                }
            }
            Rows.Add((String)tmp.DisplayedString.Clone());
            float height = tmp.GetLocalBounds().Height;
            height *= Rows.Count();// multiplied by numbers of rows

            if (height > MaxHeight)
                throw new Exception(); // so now lest check if there is enought place for rows

            row = "";//clearing row
            foreach (string item in Rows)
            {
                row += item + " ";// creating result
            }

            return row;
        }
